#include "libft.h"

void		ft_putbits_int(int j)
{
	int				i;

	i = 32;
	while (i--)
	{
		ft_putchar(j & 1 << i ? '1' : '0');
		if (i % 8 == 0 && i != 0)
			write(1, " ", 1);
	}
	write(1, "\n", 1);
}
