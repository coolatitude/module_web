#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t j;

	i = 0;
	j = (size_t)ft_strlen(s1);
	while (*(s2 + i) && (i != n))
		*(s1 + j++) = *(s2 + i++);
	*(s1 + j) = '\0';
	return (s1);
}
