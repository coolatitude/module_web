#include "libft.h"

void	ft_putstr(char const *s)
{
	int		i;
	char	*str;

	if (!s)
		return ;
	i = 0;
	str = (char *)s;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}
